numbers = [1,3, 4, 5, 6, -1, 7, 9, 100, 22, 103, 302]

# max => maximum of the numbers
# min => minimum of the numbers
print(max(numbers), min(numbers))

# sum => get the sum of the numbers
print(sum(numbers))

# len => get the count
print(len(numbers))

# what if i want average?
s = sum(numbers)
c = len(numbers)
print(s/c)
